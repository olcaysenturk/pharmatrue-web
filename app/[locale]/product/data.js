export function shapeProductForLocale(product, locale) {
  const messages = getMessages(locale);
  const copy = messages.products?.[product.translationKey] ?? {};

  return {
    ...product,
    name: copy.name ?? product.fallbackName,
    description: copy.description ?? "",
    activeIngredient: copy.activeIngredient ?? product.activeIngredient,
    size: copy.size ?? product.size,
    ctaUrl: copy.ctaUrl ?? product.ctaUrl ?? null,
    kubUrl: product.kubUrl ?? copy.kubUrl ?? null,
    brand: product.brand,
  };
}
